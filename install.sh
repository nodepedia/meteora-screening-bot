OKX_MAX_SUSPICIOUS_PCT=$OKX_MAX_SUSPICIOUS_PCT
OKX_REQUIRE_NOT_WASH=$OKX_REQUIRE_NOT_WASH

RUGCHECK_API_BASE=https://api.rugcheck.xyz/v1
RUGCHECK_MAX_SCORE=$RUGCHECK_MAX_SCORE
RUGCHECK_MAX_WARN_RISKS=$RUGCHECK_MAX_WARN_RISKS
RUGCHECK_REQUIRE_ZERO_DANGER_RISKS=$RUGCHECK_REQUIRE_ZERO_DANGER_RISKS
RUGCHECK_REQUIRE_NOT_HONEYPOT=$RUGCHECK_REQUIRE_NOT_HONEYPOT
RUGCHECK_REQUIRE_IMMUTABLE_METADATA=$RUGCHECK_REQUIRE_IMMUTABLE_METADATA

