TELEGRAM_NOTIFY_STARTUP="$(prompt "TELEGRAM_NOTIFY_STARTUP" "true")"
TELEGRAM_NOTIFY_RESULTS="$(prompt "TELEGRAM_NOTIFY_RESULTS" "true")"
