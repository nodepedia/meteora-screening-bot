TELEGRAM_CHAT_ID=$TELEGRAM_CHAT_ID
TELEGRAM_NOTIFY_STARTUP=$TELEGRAM_NOTIFY_STARTUP
TELEGRAM_NOTIFY_RESULTS=$TELEGRAM_NOTIFY_RESULTS
TELEGRAM_NOTIFY_EMPTY=$TELEGRAM_NOTIFY_EMPTY
